<h3 id="数据库" tabindex="-1"><a class="header-anchor" href="#数据库"><span>数据库</span></a></h3>
<p>数据库就是存放数据的库，负责存储、管理和检索数据。</p>
<p>我们平时看到的网站上的数据，基本都是存放到数据库中的，在用户需要的时候，再通过后端程序从数据库中取出来。</p>
<p>数据库又分为关系型数据库和非关系型数据库。关系型数据库适用于存储相互之间有关联的数据，比如做一个学生管理系统，要存储学生、班级，一个班级有多个学生，这就是一种关联关系，关系型数据库典型的技术是 MySQL；而非关系型数据库主要用于存储零散的、相互独立的数据，典型的技术是 Redis。</p>
<p>此处我们主要学习的是关系型数据库，并且以最主流、学习成本相对更低的关系型数据库 MySQL 为主。</p>
<p>对 C++ 同学来说，对数据库的学习包括以下 4 个方面：</p>
<ol>
<li>学习关系型数据库理论：包括关系模型、关系代数、数据库范式、事务理论等，能够帮助你更好地理解数据库的结构和设计。</li>
<li>SQL 语言：作为关系型数据库的标准查询语言，掌握 SQL 是非常重要的！学习过程以实践为主，要能够熟练使用 SQL 来操作数据库（增删改查）</li>
<li>数据库设计和编程：学习如何根据实际业务场景来设计数据库表、并且学习如何使用 C++ 来操作数据库，这是企业开发中最重要的能力。</li>
<li>数据库高级：初学数据库时这部分可以跳过。主要是学习数据库的高级特性、底层原理、数据库的分析、运维、性能优化等，可以帮助你更好地运用数据库来解决问题。</li>
</ol>
<p>详细学习路线请参考：<a href="https://www.code-nav.cn/post/1641797333479903234" target="_blank" rel="noopener noreferrer">https://www.code-nav.cn/post/1641797333479903234<ExternalLinkIcon/></a></p>
