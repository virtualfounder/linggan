<h2 id="学习建议" tabindex="-1"><a class="header-anchor" href="#学习建议"><span>学习建议</span></a></h2>
<ol>
<li>基础一定要打牢！这是 C++ 相对于 Java 这种上层语言来说最重要的一个建议，因为 C++ 可以更灵活地操作系统（比如自主分配内存），所以如果基础不牢固，你不仅不能发挥出 C++ 的高性能等特性，反而还可能写出更辣鸡的程序。像 C++ 基础特性（指针、引用等）、操作系统、计算机网络、算法数据结构等计算机基础都要认真去学。</li>
<li>其他的建议就不是 C++ 的专属了，学任何编程语言都一样，要多练习语法、多学习类库框架、多实践做项目、多读别人的项目和开源代码、多积累总结等等。</li>
</ol>
