<p>初学编程、刚开始学习算法时，可以先通过阅读课外书、科普视频来培养兴趣，简单地入门，而不要求真正的理解。</p>
<p>推荐阅读<a href="https://zh.u1lib.org/book/18051247/c74b8f" target="_blank" rel="noopener noreferrer">《漫画算法：小灰的算法之旅》<ExternalLinkIcon/></a> ，轻松幽默。</p>
<h3 id="依次学习" tabindex="-1"><a class="header-anchor" href="#依次学习"><span>依次学习</span></a></h3>
<p>了解每个数据结构和算法的概念、特点、适用场景、时空复杂度，并且能够自己写代码从 0 到 1 实现一遍每个数据结构和算法。注意要按顺序学习，而不是一次性看完所有的数据结构和算法后才再回过头来写代码！</p>
<p>要重点学的知识点基本就是大纲中提到的那些，图论这一块了解即可，优先级不高，面试考的也不多。链表、树是面试重点。</p>
<p>推荐直接从 LeetCode 的学习板块 LeetBook 开始刷起，边学边写代码，学的更扎实。</p>
