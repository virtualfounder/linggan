<h3 id="依次练习" tabindex="-1"><a class="header-anchor" href="#依次练习"><span>依次练习</span></a></h3>
<p>学完某个数据结构和算法后，立刻趁热打铁去做几道相关的题目。</p>
<p>每道题目给自己一个时间限制，超出时间后就直接去阅读题解（建议看带有图片和动画演示的题解），有一些思路后再试着自己去做题，直到把题目完成写出，并且要真正理解每道题目的解法。</p>
<p>即使是自己做出的题目，也建议去看下其他同学的题解，多积累一种方法。</p>
<h3 id="刷题" tabindex="-1"><a class="header-anchor" href="#刷题"><span>刷题</span></a></h3>
<p>当把每个知识点学完一遍后，可以去 LeetCode 网站的题库中刷题，按照难易度、通过率、热门程度每天坚持刷几道。时间紧张（三个月内）的话也可以直接去刷热门题目集合。</p>
<p>有时间的话，建议大家可以参加 <a href="https://www.lanqiao.cn/" target="_blank" rel="noopener noreferrer">蓝桥杯竞赛<ExternalLinkIcon/></a> 、考 <a href="https://www.patest.cn/" target="_blank" rel="noopener noreferrer">PAT 认证<ExternalLinkIcon/></a>，检验自己的学习、丰富自己的简历。</p>
<h4 id="刷题顺序" tabindex="-1"><a class="header-anchor" href="#刷题顺序"><span>刷题顺序</span></a></h4>
<blockquote>
<p>仅供参考，可以有自己的学习方法和习惯</p>
</blockquote>
<ol>
<li>先按知识点逐个去刷，可以进入 <a href="https://leetcode-cn.com/leetbook/" target="_blank" rel="noopener noreferrer">https://leetcode-cn.com/leetbook/<ExternalLinkIcon/></a>，这里有按知识点分类的教程和题目，比如初级算法、数组、字符串、队列、栈、树等等。每刷完一个知识点，可以再做几道相关的题目，举一反三，每道题目都要做到 100% 通过。</li>
<li>把所有知识点逐个刷完后，可以再按照难度、通过率去自由刷题，刷大概几十道简单题目后，可以去做 LeetCode 热题 100 道：<a href="https://leetcode-cn.com/problem-list/2cktkvj/" target="_blank" rel="noopener noreferrer">https://leetcode-cn.com/problem-list/2cktkvj/<ExternalLinkIcon/></a>。当然，时间紧迫的话，也可以直接刷热题。</li>
<li>算法最好刷 200 道题以上，每天 2 - 3 道，坚持 2 个月以上，并且还要整理好自己的代码、定期复习。尽量不要去刷冷门的题目！别逞能去刷那么多 Hard 题，性价比不高，不如省点时间去做项目。</li>
<li>刷完 LeetCode 经典题目后，可以去刷刷《剑指 Offer》或者《剑指 Offer 面试精装版》进行巩固。</li>
</ol>
