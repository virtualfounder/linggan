<h3 id="_2、养成自己的刷题习惯" tabindex="-1"><a class="header-anchor" href="#_2、养成自己的刷题习惯"><span>2、养成自己的刷题习惯</span></a></h3>
<p>的确，学算法是枯燥的，想长期坚持非常难，像灵感虚拟发现现实当时刷了 1000 多道不同平台的题目，现在回过头来都觉得不可思议。</p>
<p>分析下我能够坚持刷这么多题的原因，主要是因为养成了自己的刷题习惯，把刷算法当成了像刷牙洗脸一样的日常任务。</p>
<p>怎么养成自己的刷题习惯呢？</p>
<p>1）每天在固定的时间学习算法。比如我当时每天早上 6 点多就会躺在床上思考没解决的算法题，想到方案后就会拿个枕头靠在床上敲代码做题，其实都有点魔怔了哈哈。</p>
<p>2）给自己定每日的学习目标。比如我每天会花 1 个半小时左右完成 3 道题目，不多也不少。如果没做完，就等其他工作完成后再静下心来思考；如果超前达到目标，那么可以奖励自己一顿番茄炒蛋。</p>
<p>3）分享自己的学习记录。比如我当时每刷几十道题目，就会跟朋友小小 “炫耀” 一下，看着自己的打卡表越来越满，也会有不小的成就感。</p>
<h3 id="_3、找到自己的灯塔" tabindex="-1"><a class="header-anchor" href="#_3、找到自己的灯塔"><span>3、找到自己的灯塔</span></a></h3>
<p>如果一个人学算法觉得很累，那么就一定要找到能伴你共同前行的伙伴，或者给你指路的灯塔。</p>
<p>可能是和你一起努力刷题的伙伴，可能是一个算法交流圈子，可能是一位专业的算法导师。</p>
<p>说不定一个问题你自己思考 30 分钟还没办法解决，而请教他人 5 分钟就能解决了，能大大节约你的时间。</p>
<p>至于一些三天打鱼两天晒网、动不动就摆烂的人，还是敬而远之。</p>
<h3 id="_4、学会总结" tabindex="-1"><a class="header-anchor" href="#_4、学会总结"><span>4、学会总结</span></a></h3>
<p>解决学算法学了就忘的最好方法，就是多记录总结，把知识沉淀成电子文档，而不是全部装在大脑里。</p>
<p>不要觉得写总结很麻烦，记录自己的解题思路、或者在代码上补充详细的注释都是可以的。如果你连自己的思路都表述不清楚，那么说明这道题你就没有理解！打回去重做！</p>
<p>可以通过云文档、GitHub 仓库、或者各平台的发帖来总结分享自己的题解和心得，费曼学习法，能够给别人讲清楚题目的做法，你自己的印象就会更深刻。</p>
<p>这样做了之后，哪怕刷了算法就忘，也能快速通过自己的笔记总结捡起来当时的思路，大幅提高复习效率。</p>
<h3 id="_5、利用题解" tabindex="-1"><a class="header-anchor" href="#_5、利用题解"><span>5、利用题解</span></a></h3>
<p>LeetCode 平台的每道题目都有很多小伙伴给出了题解，讲述如何解决这道题。</p>
<p><strong>怎么才算利用好题解呢？</strong></p>
<p>首先，读题解包括两个部分，<strong>读思路</strong> 和 <strong>读代码</strong>，既要理解作者做题的思路和逻辑，也要细致入微地学习他人代码中优秀的写法。即使这道题目你做出来了，击败 100% 了，我也建议去看看别人的题解，学习更多他人解题的思路，帮助自己打开脑洞，做到 <strong>一题多解</strong>。</p>
<p>话说，现在网上的题解实在是太多啦！在刷题时，读个一两份就行了，别给自己太大压力。</p>
<p>除了看题解外，很多同学没有意识到，多写题解 才是真正的法宝，把自己的解题思路整理成文，或者讲给别人听。这样做不仅能够加深自己对题目的印象，进一步加深对算法的理解，帮助自己回顾解题过程，从而在面试的时候更容易复述；还能帮助到更多同学。</p>
<p>甚至有一些厉害的同学通过记录和分享自己的题解，还没毕业，就已经出版了自己的书籍，年入几十万或者百万！</p>
<p>写题解利人利己，何乐而不为呢？</p>
<h3 id="_6、精益求精" tabindex="-1"><a class="header-anchor" href="#_6、精益求精"><span>6、精益求精</span></a></h3>
<p>当你每次成功解题时，LeetCode 系统会生成一份解题报告，告诉你的程序在时间和空间上击败了多少用户。</p>
<p>虽然答出题目就已经很棒了，但还不够。面试的时候，一些面试官就喜欢给你出题目的变种，或者要求你用更优的方式解出题目。所以，在保证完全理解题目解法的基础上，请不断优化你的代码，找到更多的思路和更优解，直到击败 100% 的用户吧。</p>
<p>我们在工作中，虽然未必会直接和算法打交道，但学习算法对工作的帮助真的很大！</p>
<p>解算法题时，我们要对多种算法分析复杂度，从中选择最优解。而在工作中，也是如此，一个需求有很多种实现方式，经常也要设计几种不同的方案，分析他们的成本、性能等差异，选择其中最好的一种进行实施。</p>
<p>所以，请认真对待每一道算法题目，把它当成一个工程问题来解决，相信你的思维会逐渐打开，并逐渐掌握编写高性能程序的技巧。</p>
