<h2 id="什么时候学数据结构和算法" tabindex="-1"><a class="header-anchor" href="#什么时候学数据结构和算法"><span>什么时候学数据结构和算法？</span></a></h2>
<p>时间足够的话，我个人建议 **学完一门编程语言后 **就可以开始刷算法了。一方面是因为算法真的是太重要了；另一方面是用 Java 刷算法还能巩固一下语法基础，避免出现纸上谈兵、后面做项目时因为不熟悉语法导致写不出代码的情况。</p>
<p>完全零基础的话，数据结构和算法不是面试前短短几周就能准备过来的（除非你很聪明）。灵感虚拟发现现实是从大二上开始跟着学校学习数据结构（虽然我没怎么听课，是靠后面自己看书补回来的），并且从大二暑假开始刷题，一年多的时间总共刷了 1000 多道算法题。因为自己准备得早，所以学得还算比较扎实，面试时遇到的算法题目基本都能答出来；也是因为准备得早，每天的压力没那么大。</p>
