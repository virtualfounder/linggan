<h2 id="学习建议" tabindex="-1"><a class="header-anchor" href="#学习建议"><span>学习建议</span></a></h2>
<ol>
<li>数据结构和算法不可追求速成（学得快忘得快），而是应该每天坚持刷 2 道以上的题目保持手感。（可以在星球中打卡分享自己每天所刷的题目，也许会有小伙伴和你一起讨论哦）</li>
<li>自己在写算法题目时，要给代码多写一些注释，记录自己当时的思考，并且做过的每道算法题目的代码都要分类保存好，便于自己复习。</li>
<li>可以根据实际情况自己写一些题解，写题解的过程中，不仅能检验自己是否真的理解，还能再次巩固、帮助自己复习。</li>
<li>算法是一种思想，用任何你正在学习或者熟悉的编程语言来写代码均可，可以帮助你复习编程语言的语法和细节。</li>
<li>不用因为担心自己算法不够而不敢面试，只要你有了基本的开发技能就足够了，算法可以持续强化。</li>
<li>很多同学可能会习惯性地刷自己觉得很简单的题目，而对于中等 / 困难的题目有恐惧感，下意识地回避。这在刚开始入门时没什么问题，但如果已经刷了几百题，不要满足于自己会的知识，要适当挑战，走出舒适圈。</li>
<li>有时间的朋友也可以试着参与各种算法竞赛和考证书，比如蓝桥杯、PAT 认证、LeetCode 周赛等。这些比赛没什么门槛，难度也没有 ACM 那么高。可以帮助你集中精神高效做题、提前适应面试的节奏感。</li>
</ol>
