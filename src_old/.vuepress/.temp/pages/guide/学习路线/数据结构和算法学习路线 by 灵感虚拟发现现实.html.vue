<h2 id="更多学习建议" tabindex="-1"><a class="header-anchor" href="#更多学习建议"><span>更多学习建议</span></a></h2>
<h3 id="_1、刷题顺序" tabindex="-1"><a class="header-anchor" href="#_1、刷题顺序"><span>1、刷题顺序</span></a></h3>
<p>最直接的就是按照知识点标签选题，从【简单的】题目刷起，从【通过率高】的题目刷起，循序渐进。</p>
