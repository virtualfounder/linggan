<p>为什么数据结构和算法如此重要？因为：程序 = 数据结构 + 算法</p>
<p>数据结构是一组数据的存储结构和组织方式，使得该组数据便于访问、修改、存储。</p>
<p>算法是操作数据的一组方法，解决问题的一系列步骤。</p>
<h2 id="学习的必要性" tabindex="-1"><a class="header-anchor" href="#学习的必要性"><span>学习的必要性</span></a></h2>
<p>有些同学可能会因为平时写代码用不到数据结构和算法，就认为它们不重要，这是**一个错误的认识 **！</p>
<p>其实很多时候，并不是用不到，而是因为你欠缺了一部分知识后，根本就想不到去用它来优化你的程序，比如提高性能、节省内存等。</p>
<p>还有一个平时很少用到算法的原因是：其他开发者给你封装好了。但假如有一天，你用的框架或算法库出了问题、或者某个算法效率低下，需要你来优化的时候，如果没学过数据结构和算法你大概率是搞不定的。</p>
<p>换句话说，不是不报，是时候未到。学好算法和数据结构，有助于开拓我们的思路，改变我们思考问题的方式，提高我们的问题解决能力（公司最看重的也是这点）。</p>
<p>至少我觉得，学习数据结构和算法让我受益匪浅。</p>
