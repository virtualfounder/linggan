<h3 id="✅-性能优化" tabindex="-1"><a class="header-anchor" href="#✅-性能优化"><span>✅ 性能优化</span></a></h3>
<h4 id="性能指标" tabindex="-1"><a class="header-anchor" href="#性能指标"><span>性能指标</span></a></h4>
<ul>
<li>💬 描述：用于衡量一个 Web 页面的性能。</li>
<li>📚 资源：<a href="https://juejin.cn/post/6844904153869713416" target="_blank" rel="noopener noreferrer">https://juejin.cn/post/6844904153869713416<ExternalLinkIcon/></a></li>
<li>FP（First Paint）
<ul>
<li>💬 描述：从开始加载到浏览器首次绘制像素到屏幕上的时间，也就是页面在屏幕上首次发生视觉变化的时间。</li>
</ul>
</li>
<li>FCP（First Contentful Paint）
<ul>
<li>💬 描述：浏览器首次绘制来自 DOM 的内容的时间。</li>
</ul>
</li>
<li>FMP（First Meaningful Paint）
<ul>
<li>💬 描述：页面的主要内容绘制到屏幕上的时间。</li>
</ul>
</li>
<li>FSP（First Screen Paint）
<ul>
<li>💬 描述：页面从开始加载到首屏内容全部绘制完成的时间，用户可以看到首屏的全部内容。</li>
</ul>
</li>
<li>TTI（Time to Interactive）
<ul>
<li>💬 描述：表示网页第一次完全达到可交互状态的时间点，浏览器已经可以持续性的响应用户的输入。</li>
</ul>
</li>
</ul>
