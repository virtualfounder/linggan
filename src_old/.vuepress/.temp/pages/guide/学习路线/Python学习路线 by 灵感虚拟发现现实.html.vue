<h2 id="python-为什么火了" tabindex="-1"><a class="header-anchor" href="#python-为什么火了"><span>Python 为什么火了？</span></a></h2>
<p>有很多原因，列举几点：</p>
<ol>
<li>语法简单易学，其他语言 5 行代码才能实现的东西，Python 一行搞定！可以少写很多代码，因此不少同学拿 Python 刷算法题（但注意要理解算法本身哦）。</li>
